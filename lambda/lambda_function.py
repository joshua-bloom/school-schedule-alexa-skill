class LaunchRequestHandler(AbstractRequestHandler):
    """Handler for Skill Launch."""
    def can_handle(self, handler_input):
        # type: (HandlerInput) -> bool

        return ask_utils.is_request_type("LaunchRequest")(handler_input)

    def handle(self, handler_input):
        logger.info("in LaunchRequestHandler()")
        # type: (HandlerInput) -> Response
        speak_output = "Welcome to Alexa Whipple Hill, to set up your school schedule, say My Classes are something, something, and something."
        reprompt_text = "Please say, My Classes are ..."
        
        handler_input.response_builder.speak(speak_output)
        handler_input.response_builder.ask(reprompt_text)

        return handler_input.response_builder.response


class CaptureScheduleIntentHandler(AbstractRequestHandler):
    """Handler for Capture Schedule Intent."""
    def can_handle(self, handler_input):
        # type: (HandlerInput) -> bool
        return ask_utils.is_intent_name("CaptureScheduleIntent")(handler_input)

    def handle(self, handler_input):
        # type: (HandlerInput) -> Response
        logger.info("in CaptureScheduleIntentHandler()")
        speak_output = "Ok. Your schedule is saved into my memory. "
        
        slots = handler_input.request_envelope.request.intent.slots
        
        # The Cake Time skill code receives the year, month, and day. 
        # Create three variables in the handler to save the slots the skill is collecting.
        slots = handler_input.request_envelope.request.intent.slots
        
        all_classes = slots["class_names"].value             # a string with all of the class names: 'biology computer science robotics'
        logger.info("**************************")
        logger.info(all_classes)
        list_of_classes = all_classes.split()
        
        courseNum = 1
        for course in list_of_classes:
            logger.info("Course #%i\t %s" % (courseNum, course))
            courseNum += 1
        
        #logger.info(slots)
        
        return (
            handler_input.response_builder
                .speak(speak_output)
                .response
        )

